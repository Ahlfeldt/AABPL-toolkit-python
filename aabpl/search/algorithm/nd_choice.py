# Point-count-weighted per-block decision (opt-in via cfg.USE_WEIGHTED_ND_DECISION)
# ---------------------------------------------------------------------------
# best_nd_tag above collapses a chunk's whole density distribution into one
# percentile (p75) before ever consulting the cost model -- correct for a
# roughly-uniform chunk, systematically misleading for one with a real
# density mixture (e.g. a dense hotspot next to a sparse background): p75 of
# occupied-block DENSITIES also can't tell "1 of 100 blocks occupied" from
# "99 of 100 blocks occupied" at the same per-block density, since it never
# sees occupancy count at all. best_nd_tag_weighted instead evaluates the
# real cost model on every occupied block's own ppc (weighted by that
# block's actual point count, so total-workload/occupancy is correctly
# reflected in the sum itself) and blends 50/50 against a total-chunk-AREA
# ppc (points / full chunk footprint including empty blocks -- occupancy-
# aware by construction, robust to any single noisy block).

_sorted_bp_cache = {}

def _get_sorted_breakpoints(sr_key, tag):
    """Cached (ppcs_sorted_array, vals_array) for one (sr_key, tag) -- computed
    once, reused across every block/chunk that queries this tag at this sr.
    _interp_ppc re-sorts timing_dict.keys() on every call; doing that once per
    (sr, tag) instead of once per (block, tag) is what makes per-block
    evaluation viable at all -- see the session discussion on this."""
    ck = (sr_key, tag)
    cached = _sorted_bp_cache.get(ck)
    if cached is not None:
        return cached
    timing_dict = _BENCH.get(sr_key, {}).get(tag, {})
    if not timing_dict:
        result = (None, None)
    else:
        ppcs = sorted(timing_dict)
        result = (_np.array(ppcs, dtype=float), _np.array([timing_dict[p] for p in ppcs], dtype=float))
    _sorted_bp_cache[ck] = result
    return result


def cost_at_array(sr_key, tag, ppc_array):
    """Vectorized _interp_ppc over an array of ppc values, for one exact
    sr_key (must be a value already present in _BENCH -- this is a single-sr
    lookup, no sr-interpolation; callers needing that blend across two nearby
    sr_key calls themselves, same as best_nd_tag does for its own crossover
    lookup). Mirrors _interp_ppc's clamp/extrapolation logic exactly.
    """
    ppc_array = _np.asarray(ppc_array, dtype=float)
    ppcs, vals = _get_sorted_breakpoints(sr_key, tag)
    if ppcs is None:
        return _np.full(ppc_array.shape, _np.inf)
    out = _np.empty(ppc_array.shape, dtype=float)

    lo_mask = ppc_array <= ppcs[0]
    out[lo_mask] = vals[0]

    hi_mask = ppc_array >= ppcs[-1]
    if hi_mask.any():
        if len(ppcs) < 2 or ppcs[-1] == ppcs[-2]:
            out[hi_mask] = vals[-1]
        else:
            lo, hi = ppcs[-2], ppcs[-1]
            log_lo, log_hi = _math.log(lo), _math.log(hi)
            val_lo, val_hi = vals[-2], vals[-1]
            if val_lo <= 0 or val_hi <= 0 or log_hi == log_lo:
                out[hi_mask] = max(val_hi, 0.0)
            else:
                slope = max(1.0, (_math.log(val_hi) - _math.log(val_lo)) / (log_hi - log_lo))
                out[hi_mask] = val_hi * _np.exp(slope * (_np.log(ppc_array[hi_mask]) - log_hi))

    mid_mask = ~lo_mask & ~hi_mask
    if mid_mask.any():
        idx = _np.searchsorted(ppcs, ppc_array[mid_mask])
        idx = _np.clip(idx, 1, len(ppcs) - 1)
        lo_ppc, hi_ppc = ppcs[idx - 1], ppcs[idx]
        t = (_np.log(ppc_array[mid_mask]) - _np.log(lo_ppc)) / (_np.log(hi_ppc) - _np.log(lo_ppc))
        out[mid_mask] = vals[idx - 1] * (1.0 - t) + vals[idx] * t

    return out


def _nearest_sr_key(sr):
    idx = int(_np.searchsorted(_SR_VALS, sr, side='right')) - 1
    idx = max(0, min(idx, len(_SR_VALS) - 1))
    return float(_SR_VALS[idx])


def best_nd_tag_weighted(sr, block_ppcs, block_counts, total_area_ppc,
                          candidate_tags=None, share_occupied=None):
    """Point-count-weighted, occupancy-aware alternative to best_nd_tag.

    block_ppcs / block_counts: per-occupied-coarse-block ppc and point count
        arrays (same length, same order) for the chunk being decided.
    total_area_ppc: points_in_chunk / full_chunk_area (fine cells, including
        empty blocks) * pi*sr^2 -- the occupancy-aware whole-chunk density.
    share_occupied: fraction of coarse blocks in the chunk's bounding box
        that are actually occupied (n_occupied / n_blocks_possible, all
        blocks counted whether populated or not). Drives how much to trust
        total_area_ppc -- see alpha below. Falls back to a raw-count-based
        proxy (old behaviour) if not supplied, e.g. by callers that haven't
        computed the full bounding-box block count.
    candidate_tags: which (nd, single_region) tags to compare; defaults to
        every tag with benchmark data (_ALL_TAGS). Narrowing this is the
        obvious next lever if the full sweep proves too expensive.

    Respects FIXED_NEST_DEPTH the same way best_nd_tag does (short-circuits
    before touching any of the above), so fixed-nd testing is unaffected.
    """
    _fixed_nd = getattr(_cfg, 'FIXED_NEST_DEPTH', None)
    if _fixed_nd is not None:
        return (_fixed_nd, bool(getattr(_cfg, 'SINGLE_REGION', False)))

    if len(block_ppcs) == 0:
        return best_nd_tag(sr, max(total_area_ppc, 1e-6))

    sr_key = _nearest_sr_key(sr)
    block_ppcs = _np.asarray(block_ppcs, dtype=float)
    block_counts = _np.asarray(block_counts, dtype=float)
    total_pts = float(block_counts.sum())
    total_area_ppc = max(total_area_ppc, 1e-6)
    tags = candidate_tags if candidate_tags is not None else _ALL_TAGS

    # alpha = weight on the occupancy-aware total-area-ppc term (the
    # "average" side); (1-alpha) weights the point-count-weighted per-block
    # sum (the "mixture" side). total_area_ppc divides total points by the
    # FULL bounding-box area, empty blocks included -- when most blocks are
    # occupied that's close to an unbiased local-density estimate (trust it);
    # when points cluster in a few blocks out of many, the same average is
    # diluted by all that empty space and badly understates density where
    # points actually are (defer to the per-block mixture sum instead).
    # share_occupied (not raw n_occupied) is what actually captures this: 42
    # occupied blocks out of 50 (dense coverage) and 42 out of 5000 (sparse,
    # clustered) have the same n_occupied but call for opposite trust in the
    # area average.
    _n_occupied = len(block_ppcs)
    if share_occupied is None:
        # Fallback for callers that haven't computed the full bounding-box
        # block count (old count-only heuristic).
        alpha = 0.1 + 0.9 / (1.0 + _n_occupied ** 0.5)
    else:
        alpha = 0.9 * max(0.0, min(1.0, share_occupied))

    best_tag, best_cost = None, float('inf')
    for tag in tags:
        cost = blended_cost_ms(sr_key, tag, block_ppcs, block_counts, total_area_ppc, alpha,
                                total_pts=total_pts)
        if cost < best_cost:
            best_cost = cost
            best_tag = tag
    return best_tag


def blended_cost_ms(sr_key, tag, block_ppcs, block_counts, total_area_ppc, alpha, total_pts=None):
    """Estimated total processing ms for one specific `tag` on this chunk (or
    chunk-piece), using the same point-count-weighted-sum / occupancy-aware-
    average blend as best_nd_tag_weighted's own internal tag search -- shared
    so any other cost comparison (e.g. the row-strip/column-band merge
    decision in disk_aggregation_chunk_adaptive_nd.py) reasons about cost
    with the same model actually used to pick nd, instead of a cheaper but
    inconsistent single-ppc estimate.

    sr_key: an sr value already resolved via _nearest_sr_key (or an exact
        _BENCH key) -- not a raw runtime sr.
    block_ppcs / block_counts / total_area_ppc: see best_nd_tag_weighted.
    alpha: already resolved (0..1), e.g. 0.9 * share_occupied -- see
        best_nd_tag_weighted for how this is normally derived.
    total_pts: optional -- pass to skip recomputing sum(block_counts) when
        the caller already has it.
    """
    block_ppcs = _np.asarray(block_ppcs, dtype=float)
    block_counts = _np.asarray(block_counts, dtype=float)
    if total_pts is None:
        total_pts = float(block_counts.sum())
    total_area_ppc = max(total_area_ppc, 1e-6)
    per_block_cost = cost_at_array(sr_key, tag, block_ppcs)
    sum_cost = float(_np.dot(block_counts, per_block_cost))
    avg_cost = total_pts * float(cost_at_array(sr_key, tag, _np.array([total_area_ppc]))[0])
    return (1.0 - alpha) * sum_cost + alpha * avg_cost


# ---------------------------------------------------------------------------
# Documentation plot: chosen nd across the full ppc range for a given sr.
# ---------------------------------------------------------------------------
def plot_nd_choice_curve(sr=2.0, ppc_lo=0.001, ppc_hi=1_000_000, n_points=600,
                          out_path=None, show=False):
    """Plot best_nd_tag(sr, ppc)'s chosen nd across ppc_lo..ppc_hi (log scale).

    Line color encodes single_region (True/False). Markers overlay the real
    benchmark observations backing this decision (from _BENCH[sr]) at
    whichever ppc values were actually tested -- the empirically-best tag at
    each tested ppc, so markers sit on the curve exactly where it reflects
    real data rather than the get_exact_L extrapolation (see best_nd_tag).

    Saves a PNG to out_path (default: docs/img/nd_choice_by_ppc_sr{sr}.png
    relative to the repo root) unless out_path is False. Returns the
    matplotlib Figure. Requires matplotlib (imported lazily -- not a hard
    dependency of the library, only of this documentation helper).
    """
    import matplotlib
    matplotlib.use('Agg')
    import matplotlib.pyplot as plt
    from matplotlib.lines import Line2D

    # The curve is just best_nd_tag itself, sampled densely -- best_nd_tag
    # uses _build_nd_curve internally for any sr that exactly matches a
    # benchmarked value, so this plot always reflects real decisions rather
    # than a separately-maintained approximation of them.
    ppcs = _np.geomspace(ppc_lo, ppc_hi, n_points)
    nds = _np.empty(n_points)
    sregs = _np.empty(n_points, dtype=bool)
    for i, ppc in enumerate(ppcs):
        nd, sreg = best_nd_tag(sr, float(ppc))
        nds[i], sregs[i] = nd, sreg

    # Real benchmark observations, for the scatter markers only (the curve
    # itself already incorporates these via _build_nd_curve).
    obs_ppc, obs_nd, obs_sreg = [], [], []
    if sr in _BENCH:
        all_ppc = sorted({p for d in _BENCH[sr].values() for p in d if p in _STANDARD_PPC_GRID})
        for p in all_ppc:
            best_t, best_v = None, float('inf')
            for tag, d in _BENCH[sr].items():
                v = d.get(p)
                if v is not None and v < best_v:
                    best_v, best_t = v, tag
            if best_t is not None:
                obs_ppc.append(p)
                obs_nd.append(best_t[0])
                obs_sreg.append(best_t[1])
    obs_ppc_arr = _np.array(obs_ppc)
    obs_nd_arr = _np.array(obs_nd)
    obs_sreg_arr = _np.array(obs_sreg, dtype=bool)

    color_true, color_false = '#2563eb', '#dc2626'   # blue = single_region, red = multi-region

    fig, ax = plt.subplots(figsize=(9, 5), dpi=150)

    # Draw the line in constant-color segments so the color genuinely
    # switches at each single_region transition instead of blending.
    seg_start = 0
    for i in range(1, n_points + 1):
        if i == n_points or sregs[i] != sregs[seg_start]:
            c = color_true if sregs[seg_start] else color_false
            ax.plot(ppcs[seg_start:i], nds[seg_start:i], color=c, linewidth=2.2,
                     solid_capstyle='round', zorder=2)
            seg_start = i

    if len(obs_ppc_arr):
        for mask, c in [(obs_sreg_arr, color_true), (~obs_sreg_arr, color_false)]:
            if mask.any():
                ax.scatter(obs_ppc_arr[mask], obs_nd_arr[mask], color=c, edgecolor='white',
                           linewidth=1.0, s=55, zorder=3)

    from matplotlib.ticker import MultipleLocator, LogLocator

    ax.set_xscale('log')
    ax.set_xlabel('ppc (points per search circle)', fontsize=11)
    ax.set_ylabel('chosen nd', fontsize=11)
    ax.set_title(f'Adaptive nd choice vs. point density (sr={sr:g})', fontsize=13, fontweight='bold')

    # y: integer nd steps only, no fractional gridlines.
    ax.yaxis.set_major_locator(MultipleLocator(1))

    # x: explicit minor ticks at 2..9 within each decade (log scale doesn't
    # always show these by default depending on backend/range), so the grid
    # includes the smaller sub-decade steps, not just each power of 10.
    ax.xaxis.set_minor_locator(LogLocator(base=10.0, subs=tuple(range(2, 10)), numticks=100))

    ax.grid(True, which='major', linestyle='-', linewidth=0.5, alpha=0.35)
    ax.grid(True, which='minor', linestyle=':', linewidth=0.3, alpha=0.2)
    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)

    legend_elems = [
        Line2D([0], [0], color=color_true, lw=2.2, label='single_region = True'),
        Line2D([0], [0], color=color_false, lw=2.2, label='single_region = False'),
        Line2D([0], [0], marker='o', color='none', markerfacecolor='#6b7280',
               markeredgecolor='white', markersize=8, label='benchmark observation'),
    ]
    ax.legend(handles=legend_elems, loc='upper left', frameon=True, fontsize=9.5)

    fig.tight_layout()

    if out_path is not False:
        if out_path is None:
            _repo_root = _os.path.abspath(_os.path.join(_os.path.dirname(__file__), '..', '..', '..'))
            out_path = _os.path.join(_repo_root, 'docs', 'img', f'nd_choice_by_ppc_sr{sr:g}.png')
        _os.makedirs(_os.path.dirname(out_path), exist_ok=True)
        fig.savefig(out_path, bbox_inches='tight')
        print(f'saved: {out_path}')

    if show:
        plt.show()
    return fig
