# Precomputed crossover thresholds per sr
# ---------------------------------------------------------------------------
# _CROSSOVERS[sr] = sorted list of (ppc_lower_bound, best_tag) transitions.
# First entry always starts at ppc=1.  Lookup: find last entry with
# ppc_lower_bound <= query_ppc.

def get_exact_L(ppc, sr):
    """Aggregation depth L (nd=-L) at which expected points/supercell crosses 1.

    ppc is defined relative to the search circle (ppc = density * pi * r^2),
    not the grid cell -- a fine cell has area (r/sr)^2 = r^2/sr^2, so the
    fine-cell density is ppc/(pi*sr^2), not ppc itself. Each smNT level
    doubles cell spacing -> 4x cell area -> 4x expected points, so
    pts_per_supercell_at_L = ppc/(pi*sr^2) * 4^L. Solve for L where that
    equals 1: L = log_4(pi*sr^2/ppc). (Previously this dropped the pi*sr^2
    factor entirely and just used log_4(1/ppc), which systematically
    underestimated the true crossover depth by log_4(pi*sr^2) levels --
    e.g. ~1.8 levels at sr=2, worse for larger sr -- silently leaving
    singleton-group overhead on the table for very sparse outlier data.)

    Used by best_nd_tag beyond the benchmarked range: the old supercell
    geometry builder (_expand_coarse) was combinatorial in 4^k and
    infeasible past k~10-12, but the coarse-cell-batching fast path in
    disk_aggregation_chunk_adaptive_nd.py builds no per-position geometry at
    all (just a block-radius formula + point grouping by row>>k), so there's
    no longer a depth ceiling forcing us to stay within benchmarked data.
    """
    return max(0.0, _math.log(_math.pi * sr * sr / max(ppc, 1e-300), 4))


# Deepest nd actually covered by benchmark data (most negative tag). Beyond
# this, get_exact_L takes over directly rather than extrapolating the cost
# model further — see best_nd_tag.
_DEEPEST_BENCHED_L = -min((t[0] for t in _ALL_TAGS), default=0)

# Ceiling on L. Not driven by any real limit: numpy int right-shift by >=64
# safely returns 0 (correctly meaning "the whole dataset is one supercell"),
# and coarse_spacing = grid_spacing*2**k is a plain float, fine to well past
# k=1000. The only genuine bound is get_exact_L's own floor on ppc
# (1e-300), which caps its own output at get_exact_L(0) ~= 498 — deeper than
# that is not a finer answer, just noise on an already-degenerate input.
_MAX_SUPERCELL_L = 500


def _build_crossovers():
    ppc_grid = list(_np.logspace(-4, 4, 400))  # 1e-4..10000, 400 log-spaced points
    result = {}
    for sr in _SR_VALS:
        sr = float(sr)
        sr_tags = list(_BENCH.get(sr, {}).keys())
        zones = []
        prev_tag = None
        for ppc in ppc_grid:
            best_tag, best_cost = None, float('inf')
            for tag in _ALL_TAGS:
                if tag not in sr_tags:
                    continue
                c = _cost_at(sr, tag, ppc)
                if c < best_cost:
                    best_cost = c
                    best_tag = tag
            if best_tag != prev_tag:
                zones.append((float(ppc), best_tag))
                prev_tag = best_tag
        result[sr] = zones
    return result

_CROSSOVERS = _build_crossovers()


def _smooth_crossovers(crossovers):
    """Enforce nd non-decreasing as ppc increases within each sr's zone list.

    nd should never decrease as ppc rises -- denser data never needs *less*
    subdivision/regions than sparser data. Real crossover tables sometimes
    have a local dip (e.g. sr=2.0: ...,(4159,3),(5003,2),(8313,3),... at
    ppc~5000) from measurement noise in the underlying benchmark rather than
    a genuine reversal. A running-max pass over nd removes these dips at the
    source (every caller benefits, not just documentation) without needing
    to hand-identify which specific zones are noisy.
    """
    smoothed = {}
    for sr, zones in crossovers.items():
        out = []
        running_tag = None
        running_max_nd = -_math.inf
        for ppc, tag in zones:
            nd, _single_region = tag
            if nd >= running_max_nd:
                running_max_nd = nd
                running_tag = tag
                out.append((ppc, tag))
            else:
                # This zone would dip below a depth already reached at a
                # lower ppc -- reuse the tag from whichever prior zone set
                # that running max, not this (noisy) zone's own tag, so
                # single_region stays paired with the nd it actually came
                # from.
                out.append((ppc, running_tag))
        smoothed[sr] = out
    return smoothed


_CROSSOVERS = _smooth_crossovers(_CROSSOVERS)


def _crossover_lookup(zones, ppc):
    """Binary search: return tag for this ppc in precomputed crossover list."""
    lo, hi = 0, len(zones) - 1
    while lo < hi:
        mid = (lo + hi + 1) // 2
        if zones[mid][0] <= ppc:
            lo = mid
        else:
            hi = mid - 1
    return zones[lo][1]


_curve_cache = {}

# Standard ppc grid, mirrored from bench_adaptive.py's PPC_LIST -- observations
# at off-grid ppc (e.g. the anchor*1.4-shifted checks, ratio-variant sweep
# points) aren't shown in the HTML table and shouldn't silently influence the
# curve either. Keep in sync with bench_adaptive.py's PPC_LIST by hand; there's
# no live import across the temp/ <-> aabpl/ boundary.
_STANDARD_PPC_GRID = frozenset([
    0.01, 0.1, 1, 6, 10, 20, 63, 100, 200, 300, 400, 630, 800,
    1000, 1500, 2000, 3000, 4000, 6300, 8000, 10000, 20000, 30000, 100000, 200000,
])


def _build_nd_curve(sr):
    """Single source of truth for "what nd wins at each ppc", shared by
    best_nd_tag and plot_nd_choice_curve (previously duplicated between the
    two -- the plot's construction was more careful than what best_nd_tag
    actually used at runtime, so the documentation plot didn't reflect real
    decisions; this unifies them). Cached per sr.

    Only applies when sr exactly matches a benchmarked sr (sr in _BENCH) --
    interpolating between two different sr's raw observations to build one
    combined keyframe curve isn't well-defined the same way, so best_nd_tag
    keeps the older crossover-zone blending logic for that case.

    Returns (kf_ppc, kf_nd, kf_sreg, low_offset, high_a, high_b):
      kf_*        -- sorted keyframe arrays spanning the tested ppc range.
                     Within this range, nd = kf_nd[j] where j is the last
                     index with kf_ppc[j] <= ppc (a step function).
      low_offset  -- anchors get_exact_L's formula to the curve's own value
                     at its lowest keyframe, for ppc below the tested range.
      high_a/b    -- linear trend (nd ~ a + b*log10(ppc)) fit through the
                     top 2-3 highest-ppc real observations, for ppc above
                     the tested range.

    Keyframe construction: for each tested ppc, the empirically-best tag
    among tags actually measured there. A single-point spike (nd higher
    than both immediate neighbors, reversing right back down) is treated as
    a noisy one-off rather than a real local optimum -- excluded from the
    keyframe sequence, with the resulting gap filled by synthetic
    unit-integer intermediate steps between the two flanking trusted points
    (front-loaded to reach the next confirmed value by the excluded point's
    own ppc, the highest value plausible under monotonicity, not spread
    evenly across the whole gap or left as one big leap).
    """
    if sr in _curve_cache:
        return _curve_cache[sr]

    obs_ppc, obs_nd, obs_sreg = [], [], []
    bd = _BENCH.get(sr, {})
    all_ppc = sorted({p for d in bd.values() for p in d if p in _STANDARD_PPC_GRID})
    for p in all_ppc:
        best_t, best_v = None, float('inf')
        for tag, d in bd.items():
            v = d.get(p)
            if v is not None and v < best_v:
                best_v, best_t = v, tag
        if best_t is not None:
            obs_ppc.append(p)
            obs_nd.append(best_t[0])
            obs_sreg.append(best_t[1])
    obs_ppc_arr = _np.array(obs_ppc)
    obs_nd_arr = _np.array(obs_nd)
    obs_sreg_arr = _np.array(obs_sreg, dtype=bool)

    n_obs = len(obs_nd_arr)
    is_outlier = _np.zeros(n_obs, dtype=bool)
    for i in range(1, n_obs - 1):
        if obs_nd_arr[i] > obs_nd_arr[i - 1] and obs_nd_arr[i] > obs_nd_arr[i + 1]:
            is_outlier[i] = True
    trusted_idx = [i for i in range(n_obs) if not is_outlier[i]]

    keyframes = []
    for k, i in enumerate(trusted_idx):
        keyframes.append((obs_ppc_arr[i], obs_nd_arr[i], obs_sreg_arr[i]))
        if k + 1 < len(trusted_idx):
            j = trusted_idx[k + 1]
            nd_i, nd_j = obs_nd_arr[i], obs_nd_arr[j]
            if nd_i != nd_j:
                # Never leave a >1 nd gap between consecutive trusted
                # keyframes, regardless of whether an outlier was removed
                # between them or the gap is just inherent to sparse
                # testing -- always step through every intermediate integer.
                # Arrival point: front-loaded to the last removed outlier's
                # own ppc if there was one (see docstring), else the next
                # trusted point's own ppc (nothing to front-load to).
                _outliers_between = [obs_ppc_arr[m] for m in range(i + 1, j) if is_outlier[m]]
                arrival_ppc = max(_outliers_between) if _outliers_between else obs_ppc_arr[j]
                step = 1 if nd_j > nd_i else -1
                n_steps = abs(int(nd_j - nd_i)) - 1
                if n_steps > 0:
                    log_positions = _np.linspace(
                        _math.log10(obs_ppc_arr[i]), _math.log10(arrival_ppc), n_steps + 2
                    )[1:-1]
                    for s in range(n_steps):
                        keyframes.append((10.0 ** log_positions[s], nd_i + step * (s + 1), obs_sreg_arr[i]))
                if arrival_ppc != obs_ppc_arr[j]:
                    keyframes.append((arrival_ppc, nd_j, obs_sreg_arr[i]))
    kf_ppc = _np.array([kf[0] for kf in keyframes])
    kf_nd = _np.array([kf[1] for kf in keyframes])
    kf_sreg = _np.array([kf[2] for kf in keyframes], dtype=bool)

    # low_offset below anchors to the TRUE first observation's own ppc/nd
    # (not the shifted boundary computed next), so compute it before
    # shifting kf_ppc.
    if len(kf_ppc):
        floor_L_table = float(-kf_nd[0]) if kf_nd[0] < 0 else 0.0
        floor_L_exact = get_exact_L(float(kf_ppc[0]), sr)
        low_offset = floor_L_table - floor_L_exact
    else:
        low_offset = 0.0

    if n_obs >= 2:
        top = list(zip(obs_ppc, obs_nd))[-3:] if n_obs >= 3 else list(zip(obs_ppc, obs_nd))[-2:]
        xs = _np.log10([p for p, _nd in top])
        ys = _np.array([nd for _p, nd in top], dtype=float)
        high_b, high_a = _np.polyfit(xs, ys, 1)
    else:
        high_a = high_b = 0.0

    # Shift each transition to the geometric midpoint between it and the
    # previous keyframe, so the step happens BETWEEN two competing answers
    # rather than glued exactly to one specific measurement's own ppc (a
    # step function that holds the old value right up until the next
    # keyframe's ppc, then jumps, ties the breakpoint to whichever
    # observation happens to sit on the right -- not a principled
    # crossover location). kf_ppc[0] is left alone: it's the start of the
    # trusted range itself, not a transition between two answers.
    if len(kf_ppc) > 1:
        kf_ppc = kf_ppc.copy()
        kf_ppc[1:] = _np.sqrt(kf_ppc[:-1] * kf_ppc[1:])

    result = (kf_ppc, kf_nd, kf_sreg, low_offset, float(high_a), float(high_b))
    _curve_cache[sr] = result
    return result


