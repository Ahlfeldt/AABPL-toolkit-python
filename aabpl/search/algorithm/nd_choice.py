"""
nd_choice.py — lookup-based nd selector for adaptive chunk processing.

Tags are (nd: int, single_region: bool) tuples:
  nd=-k, True  → 2^k × 2^k super-cell (coarser grid). k=1..9 is benchmarked
                 data; beyond that, best_nd_tag falls back to the exact
                 aggregation-depth formula (get_exact_L) rather than
                 extrapolating the cost model — see best_nd_tag docstring.
  nd>=0, True  → normal nd with SINGLE_REGION forced on
  nd>=0, False → normal nd, multi-region chunking

Usage::
    from .nd_choice import best_nd_tag
    tag = best_nd_tag(sr=2.0, ppc=150)   # -> (nd, single_region)
"""
import math as _math
import os as _os
import numpy as _np
from aabpl import config as _cfg
from .nd_bench_data import BENCH_DATA as _RAW_BENCH

# ---------------------------------------------------------------------------
# Benchmark data: imported from nd_bench_data.py (a plain Python literal,
# generated by temp/regen_nd_tables.py from the raw benchmark checkpoint --
# was a JSON file loaded at import time; moved to a .py module so production
# use needs no file I/O/JSON parsing, just a normal import).
#   BENCH_DATA : sr -> tag_key -> {ppc: ms_per_N}   (raw, no processing)
# tag_key is "{nd},{single_region}" e.g. "-4,True" — parsed straight back
# into the (nd, single_region) tuple used as the tag everywhere else.
# ---------------------------------------------------------------------------

def _parse_tag_key(tag_key):
    _nd_str, _sr_str = tag_key.split(',')
    return (int(_nd_str), _sr_str == 'True')


def _tag_dict(raw_sr_dict):
    return {_parse_tag_key(_tag_key): {float(_ppc): _v for _ppc, _v in _ppc_map.items()}
            for _tag_key, _ppc_map in raw_sr_dict.items()}


_BENCH = {float(_sr): _tag_dict(_tags) for _sr, _tags in _RAW_BENCH.items()}

_SR_VALS = _np.array(sorted(_BENCH.keys()), dtype=float)

# All tags observed across the benchmark data, ordered from deepest
# super-cell to highest subdivision. Built from the data itself (rather
# than hardcoded) so newly benchmarked tags are picked up automatically.
_ALL_TAGS = sorted(
    {tag for _tags in _BENCH.values() for tag in _tags},
    key=lambda t: (t[0], t[1]),
)

# ---------------------------------------------------------------------------
# Interpolation
# ---------------------------------------------------------------------------

def _interp_ppc(timing_dict, ppc):
    """Log-linear interpolation/extrapolation of ms/N at ppc.

    Clamps at the low end; extrapolates log-linearly beyond the high end,
    with the extrapolation slope floored at 1.0 in log-log space (cost can't
    grow slower than linearly in ppc — more points per cell is irreducibly
    more work). This also enforces monotonicity (result >= last measured
    value). Without the floor, tags benchmarked only over a narrow low-ppc
    range (e.g. deep super-cells, deliberately measured only at ppc<6) can
    have a sub-linear empirical slope from just 2-3 sample points, and
    extrapolating that slope 100-1000x beyond the data produces wildly
    understated costs — this is what let e.g. sm6T "win" at ppc=1000 despite
    never being benchmarked past ppc=1.
    """
    if not timing_dict:
        return float('inf')
    ppcs = sorted(timing_dict)
    if ppc <= ppcs[0]:
        return timing_dict[ppcs[0]]
    if ppc >= ppcs[-1]:
        if len(ppcs) < 2 or ppcs[-1] == ppcs[-2]:
            return timing_dict[ppcs[-1]]
        # extrapolate log-linear trend from last two breakpoints, slope floored at 1.0
        lo, hi = ppcs[-2], ppcs[-1]
        log_lo, log_hi = _math.log(lo), _math.log(hi)
        val_lo, val_hi = timing_dict[lo], timing_dict[hi]
        if val_lo <= 0 or val_hi <= 0 or log_hi == log_lo:
            return max(val_hi, 0.0)
        slope = max(1.0, (_math.log(val_hi) - _math.log(val_lo)) / (log_hi - log_lo))
        return val_hi * _math.exp(slope * (_math.log(ppc) - log_hi))
    i = 0
    while ppcs[i + 1] < ppc:
        i += 1
    lo, hi = ppcs[i], ppcs[i + 1]
    t = (_math.log(ppc) - _math.log(lo)) / (_math.log(hi) - _math.log(lo))
    return timing_dict[lo] * (1.0 - t) + timing_dict[hi] * t


def _cost_at(sr_key, tag, ppc):
    return _interp_ppc(_BENCH.get(sr_key, {}).get(tag, {}), ppc)


# ---------------------------------------------------------------------------
# Precomputed per-(sr, tag) time-per-point functions, for the chunk-merge
# cost comparison (estimate separate vs. merged processing time from ppc
# alone). _interp_ppc re-sorts its timing_dict and linear-scans for the
# bracketing breakpoints on every call -- fine for a one-off nd decision,
# wasteful if called repeatedly for the same (sr, tag) with different ppc
# values, which is exactly the merge-decision use case (every candidate
# pair, every round). get_time_fn sorts once and returns a closure over
# plain Python lists/floats (not full numpy arrays -- these timing_dicts
# are tiny, ~10-25 points, so a pure-Python bisect is faster than paying
# numpy's per-call overhead here, same lesson as this session's earlier
# array-vs-dict experiment). Same log-linear interpolation and slope-floored
# extrapolation as _interp_ppc -- this is a faster restatement of that
# function, not a different model.
# ---------------------------------------------------------------------------
_time_fn_cache = {}


def get_time_fn(sr, tag):
    """Return a cached ``f(ppc) -> ms_per_point`` closure for (sr, tag).

    Falls back to a constant-inf function if (sr, tag) has no benchmark
    data at all, so callers can treat every tag uniformly without a
    presence check. Uses exact benchmarked sr only (like _build_nd_curve --
    no cross-sr blending); pass whichever sr key matches cfg.FIXED_SPACING_RATIO.
    """
    key = (sr, tag)
    fn = _time_fn_cache.get(key)
    if fn is not None:
        return fn

    timing_dict = _BENCH.get(sr, {}).get(tag, {})
    if not timing_dict:
        fn = lambda ppc: float('inf')
        _time_fn_cache[key] = fn
        return fn

    ppcs = sorted(timing_dict)
    vals = [timing_dict[p] for p in ppcs]
    log_ppcs = [_math.log(p) for p in ppcs]
    n = len(ppcs)

    # Precompute the high-end extrapolation slope once (same floor-at-1.0
    # rule as _interp_ppc).
    if n >= 2 and vals[-1] > 0 and vals[-2] > 0 and log_ppcs[-1] != log_ppcs[-2]:
        _hi_slope = max(1.0, (_math.log(vals[-1]) - _math.log(vals[-2]))
                        / (log_ppcs[-1] - log_ppcs[-2]))
    else:
        _hi_slope = None  # signals "just return vals[-1]" (flat/degenerate case)

    def _fn(ppc, _ppcs=ppcs, _vals=vals, _logs=log_ppcs, _n=n, _hi_slope=_hi_slope):
        if ppc <= _ppcs[0]:
            return _vals[0]
        if ppc >= _ppcs[-1]:
            if _hi_slope is None:
                return _vals[-1]
            return _vals[-1] * _math.exp(_hi_slope * (_math.log(ppc) - _logs[-1]))
        # bisect for the bracketing breakpoints (timing_dicts are tiny --
        # linear scan is fine and avoids importing bisect for ~20 items).
        i = 0
        while _ppcs[i + 1] < ppc:
            i += 1
        lo, hi = _logs[i], _logs[i + 1]
        t = (_math.log(ppc) - lo) / (hi - lo)
        return _vals[i] * (1.0 - t) + _vals[i + 1] * t

    _time_fn_cache[key] = _fn
    return _fn


def estimate_time_ms(sr, tag, ppc, n_points):
    """Estimated total processing time (ms) for n_points at density ppc,
    using tag's benchmarked cost curve. Thin convenience wrapper around
    get_time_fn for one-off calls; prefer get_time_fn directly when calling
    repeatedly for the same (sr, tag) (e.g. across many candidate merges)."""
    return get_time_fn(sr, tag)(ppc) * n_points


