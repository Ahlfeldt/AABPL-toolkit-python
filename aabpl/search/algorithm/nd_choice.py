def best_nd_tag(sr, ppc):
    """Return the (nd, single_region) tag with lowest estimated cost at (sr, ppc).

    Uses precomputed crossover thresholds for O(log n) lookup.
    Interpolates between the two nearest sr table entries.

    Beyond the deepest benchmarked super-cell depth, switches to the exact
    aggregation-depth formula (get_exact_L) rather than extrapolating the
    cost model further — at that point there's no ranking data to trust, but
    the analytically "right" depth (≈1 point/cell) is unambiguous, and the
    coarse-batch fast path can build it in O(1) regardless of how deep it is.
    """
    _fixed_nd = getattr(_cfg, 'FIXED_NEST_DEPTH', None)
    if _fixed_nd is not None:
        # Single source of truth for "fixed nd" testing: short-circuit the cost
        # model entirely and force every chunk to this tag. single_region must
        # come from cfg.SINGLE_REGION here (not hardcoded False) -- callers like
        # bench_adaptive.py convey a single_region=True tag (e.g. 's1T') purely
        # via FIXED_NEST_DEPTH + SINGLE_REGION, with no monkeypatch, so dropping
        # this makes _chunk_tag's single_region bit disagree with the region
        # structure the grid was actually built with -> KeyError on
        # cntd_offset_by_region[region_trgl_id] (single-region grids only
        # populate region 0).
        return (_fixed_nd, bool(getattr(_cfg, 'SINGLE_REGION', False)))

    ppc = float(ppc)

    if sr in _BENCH:
        # Exact match to a benchmarked sr: single source of truth shared
        # with plot_nd_choice_curve (_build_nd_curve) -- both step through
        # the same outlier-filtered, intermediate-step-filled keyframes, so
        # the documentation plot always reflects what this function
        # actually decides, not a separately-maintained approximation of it.
        kf_ppc, kf_nd, kf_sreg, low_offset, high_a, high_b = _build_nd_curve(sr)
        if len(kf_ppc):
            lo_obs, hi_obs = float(kf_ppc[0]), float(kf_ppc[-1])
            if ppc > hi_obs:
                # Beyond the highest ppc ever actually benchmarked at this sr:
                # follow the measured trend from the top 2-3 real
                # observations instead of flatlining at the last keyframe,
                # floored at that keyframe's own nd so it never steps back
                # shallower than the last real measurement.
                _nd_pred = high_a + high_b * _math.log10(ppc)
                _nd = max(int(kf_nd[-1]), int(round(_nd_pred)))
                return (_nd, bool(kf_sreg[-1]))
            if ppc < lo_obs:
                # Below the lowest ppc ever tested at this sr: trust
                # get_exact_L's *shape*, anchored so it agrees exactly with
                # the curve's own value at its lowest keyframe (low_offset)
                # -- otherwise the formula's independent zero-point creates
                # a discontinuous jump right at the boundary, which then
                # partially reverses further out (a visible hump).
                _l_exact = get_exact_L(ppc, sr)
                _l = max(0, int(round(_l_exact + low_offset)))
                return (-min(_MAX_SUPERCELL_L, _l), True)
            j = int(_np.searchsorted(kf_ppc, ppc, side='right')) - 1
            j = max(0, j)
            return (int(kf_nd[j]), bool(kf_sreg[j]))

    # sr doesn't exactly match a benchmarked value (or has no observations):
    # cross-sr interpolation between the two nearest benched sr's, using the
    # older crossover-zone tables (unaffected by the above -- this path is
    # only reached for the rarer non-exact-sr case).
    _l_exact = get_exact_L(ppc, sr)
    if _l_exact > _DEEPEST_BENCHED_L:
        # Floor at _DEEPEST_BENCHED_L: this fallback only ever triggers once
        # we're already past the benchmarked depth, so the returned L should
        # never round back to something shallower than that boundary --
        # continuation from where the benchmark data leaves off, not a step
        # backward.
        _l = max(_DEEPEST_BENCHED_L, int(round(_l_exact)))
        return (-min(_MAX_SUPERCELL_L, _l), True)
    ppc = max(1e-6, ppc)
    idx = int(_np.searchsorted(_SR_VALS, sr, side='right')) - 1
    idx = max(0, min(idx, len(_SR_VALS) - 2))
    lo_sr, hi_sr = float(_SR_VALS[idx]), float(_SR_VALS[idx + 1])

    tag_lo = _crossover_lookup(_CROSSOVERS[lo_sr], ppc)
    tag_hi = _crossover_lookup(_CROSSOVERS[hi_sr], ppc)
    if tag_lo == tag_hi:
        return tag_lo

    # Near a crossover boundary: compute actual blended costs for both candidates
    t_sr = 0.0 if hi_sr == lo_sr else max(0.0, min(1.0, (sr - lo_sr) / (hi_sr - lo_sr)))
    candidates = {tag_lo, tag_hi}
    best_tag, best_cost = None, float('inf')
    for tag in candidates:
        cost = ((1.0 - t_sr) * _cost_at(lo_sr, tag, ppc)
                + t_sr        * _cost_at(hi_sr, tag, ppc))
        if cost < best_cost:
            best_cost = cost
            best_tag = tag
    return best_tag


